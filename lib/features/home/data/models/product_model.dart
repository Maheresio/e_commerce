import '../../domain/entities/product_entity.dart';
import 'review_model.dart';

class ProductModel extends ProductEntity {
  const ProductModel({
    required super.id,
    required super.name,
    required super.category,
    required super.tags,
    required super.price,
    super.discountedPrice,
   required super.discountValue,
    required super.sizes,
    required super.colors,
    required super.brand,
    required super.description,
    required super.reviewCount,
    required super.rating,
    super.reviews,
    required super.imageUrls,
    required super.isFavorite,
    required super.isInStock,
    required super.ratingsBreakdown,
    required super.gender,
    required super.createdAt,
  });

  factory ProductModel.fromMap(Map<String, dynamic> map, String documentId) {
    return ProductModel(
      id: documentId,
      name: map['name'] as String,
      category: map['category'] as String,
      tags: List<String>.from(map['tags'] ?? []),
      price: (map['price'] as num).toDouble(),
      discountedPrice: map['discountedPrice']?.toDouble(),
      discountValue: map['discountValue'] as int,
      sizes: List<String>.from(map['sizes'] ?? []),
      colors: List<String>.from(map['colors'] ?? []),
      brand: map['brand'] as String,
      description: map['description'] as String,
      reviewCount: map['reviewCount'] as int,
      rating: (map['rating'] as num).toDouble(),
      imageUrls: Map<String, List<String>>.from(
        (map['imageUrls'] ?? {}).map(
          (key, value) =>
              MapEntry(key.toString(), List<String>.from(value as List)),
        ),
      ),

      isFavorite: map['isFavorite'] as bool,
      isInStock: map['isInStock'] as bool,
      ratingsBreakdown:
          (map['ratingsBreakdown'] is Map<String, dynamic>)
              ? (map['ratingsBreakdown'] as Map<String, dynamic>).map(
                (key, value) => MapEntry(int.parse(key), value as int),
              )
              : {},
      gender: map['gender'] as String,
      createdAt: DateTime.parse(map['createdAt'] as String),
    );
  }

  Map<String, dynamic> toMap(String idd) {
    return {
      'id': idd,
      'name': name,
      'category': category,
      'tags': tags,
      'price': price,
      'discountedPrice': discountedPrice,
      'discountValue': discountValue,
      'sizes': sizes,
      'colors': colors,
      'brand': brand,
      'description': description,
      'reviewCount': reviewCount,
      'rating': rating,
      'imageUrls': imageUrls,
      'isFavorite': isFavorite,
      'isInStock': isInStock,
      'ratingsBreakdown': ratingsBreakdown.map(
        (key, value) => MapEntry(key.toString(), value),
      ),
      'gender': gender,
      'createdAt': createdAt.toIso8601String(),
    };
  }
}

